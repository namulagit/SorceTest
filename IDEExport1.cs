using System;
using System.Text;
using System.Text.RegularExpressions;
using System.Threading;
using NUnit.Framework;
using OpenQA.Selenium;
using OpenQA.Selenium.Firefox;
using OpenQA.Selenium.Support.UI;

namespace SeleniumTests
{
    [TestFixture]
    public class IDEExport1
    {
        private IWebDriver driver;
        private StringBuilder verificationErrors;
        private string baseURL;
        private bool acceptNextAlert = true;
        
        [SetUp]
        public void SetupTest()
        {
            driver = new FirefoxDriver();
            baseURL = "http://beacon-test/sorce";
            verificationErrors = new StringBuilder();
        }
        
        [TearDown]
        public void TeardownTest()
        {
            try
            {
                driver.Quit();
            }
            catch (Exception)
            {
                // Ignore errors if unable to close the browser
            }
            Assert.AreEqual("", verificationErrors.ToString());
        }
        
        [Test]
        public void TheIDEExport1Test()
        {
            driver.Navigate().GoToUrl(baseURL + "/sorce/beacon/");
            driver.FindElement(By.CssSelector("div.beaconToolbarItemWrapper.beaconToolbarItemWrapperCogmenu")).Click();
            driver.FindElement(By.CssSelector("li.beaconToolbarAddContentLIItem")).Click();
            driver.FindElement(By.XPath("//li")).Click();
            driver.FindElement(By.LinkText("System administration")).Click();
            // ERROR: Caught exception [ERROR: Unsupported command [selectWindow | name=main | ]]
            driver.FindElement(By.LinkText("Manage groups")).Click();
            driver.FindElement(By.Id("href_1844")).Click();
        }
        private bool IsElementPresent(By by)
        {
            try
            {
                driver.FindElement(by);
                return true;
            }
            catch (NoSuchElementException)
            {
                return false;
            }
        }
        
        private bool IsAlertPresent()
        {
            try
            {
                driver.SwitchTo().Alert();
                return true;
            }
            catch (NoAlertPresentException)
            {
                return false;
            }
        }
        
        private string CloseAlertAndGetItsText() {
            try {
                IAlert alert = driver.SwitchTo().Alert();
                string alertText = alert.Text;
                if (acceptNextAlert) {
                    alert.Accept();
                } else {
                    alert.Dismiss();
                }
                return alertText;
            } finally {
                acceptNextAlert = true;
            }
        }
    }
}
